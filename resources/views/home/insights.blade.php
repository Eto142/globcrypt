                <h3>What is trading psychology</h3>
                <p style="color:black">Trading psychology refers to the emotions and mental state of a trader while making trading decisions. It is a well-known fact that emotions can have a significant impact on a trader’s performance and that a trader’s psychology can make or break their success in the market. Fear, greed, and overconfidence are common emotions that can lead to poor trading decisions, and it is important for traders to be aware of these emotions and to have strategies in place to manage them.</p>
                
                
